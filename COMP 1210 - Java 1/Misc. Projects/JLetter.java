/*********************************
* This program will print the word
"java" to form the letter "J".
* @author Jacob Howard
*********************************/
public class JLetter {
/** 
* This will print the word "java"
to form the letter "J".
* @param args Command line (not used)
*/
   public static void main(String[] args) {
      System.out.println("JAVAJAVAJAVA");
   //prints 1'st line for "J"
      System.out.println("JAVAJAVAJAVA");
   //prints 2'nd line for "J"
      System.out.println("      JAVA");
      //prints 3'rd line for "J"
      System.out.println("      JAVA");
      //prints 4'th line for "J"
      System.out.println("      JAVA");
      //prints 5'th line for "J"
      System.out.println("      JAVA");
      //prints 6'th line for "J"
      System.out.println("J     JAVA");
      //prints 7'th line for "J"
      System.out.println("JA    JAVA");
      //prints 8'th line for "J"
      System.out.println(" JAVAJAVA");
      //prints 9'th line for "J"
      System.out.println("  JAVAJA");
      //prints 10'th and final line for "J"
   }
}